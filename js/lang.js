function setLanguage(lang) {
  localStorage.setItem('portfolioLang', lang);
  document.documentElement.lang = lang;
  const langSwitcher = document.getElementById('lang-switcher');
  if (langSwitcher) langSwitcher.value = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    let value = translations[lang][key];
    if (!value) {
      value = translations['en'][key] || '';
      if (!value) {
        console.warn(`Missing translation for key: ${key} in language: ${lang}`);
      }
    }
    if (key === 'heroHeadline') {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });
  // Translate tech badges if they have data-i18n
  document.querySelectorAll('.tech-badge[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    let value = translations[lang][key] || translations['en'][key] || '';
    el.textContent = value;
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const lang = localStorage.getItem('portfolioLang') || 'en';
  setLanguage(lang);
  document.getElementById('lang-switcher').addEventListener('change', e => {
    setLanguage(e.target.value);
  });
}); 